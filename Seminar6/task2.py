# В результате 10 независимых измерений некоторой величины X, 
# выполненных с одинаковой точностью, получены опытные данные: 
# 6.9, 6.1, 6.2, 6.8, 7.5, 6.3, 6.4, 6.9, 6.7, 6.1 Предполагая, 
# что результаты измерений подчинены нормальному закону распределения 
# вероятностей, оценить истинное значение величины X при помощи 
# доверительного интервала, покрывающего это значение с доверительной 
# вероятностью 0,95.

import numpy as np
import scipy.stats as stats

# Заданные значения
data = np.array([6.9, 6.1, 6.2, 6.8, 7.5, 6.3, 6.4, 6.9, 6.7, 6.1])
confidence_level = 0.95

# Вычисляем выборочное среднее и стандартное отклонение
M = np.mean(data)
sigma = np.std(data, ddof=1)

# Вычисляем количество измерений
n = len(data)

# Вычисляем стандартную ошибку
standard_error = sigma / (n ** 0.5)

# Вычисляем критическое значение t-статистики
t_critical = stats.t.ppf((1 + confidence_level) / 2, n - 1)

# Вычисляем границы доверительного интервала
lower_bound = M - t_critical * standard_error
upper_bound = M + t_critical * standard_error

# Вывод результатов
print("Доверительный интервал:", (lower_bound, upper_bound))