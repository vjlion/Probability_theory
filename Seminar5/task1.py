
# Когда используется критерий Стьюдента, а когда Z –критерий?

# Критерий Стьюдента используется, когда дисперсия генеральной 
# совокупности неизвестна, а выборочная размерность меньше 30. 
# критерий Z используется, когда дисперсия генеральной совокупности 
# известна и/или выборочная размерность больше 30. Методы Стьюдента 
# и Z-критерий могут использоваться в одних и тех же ситуациях в 
# зависимости от наличия знаний о генеральной совокупности и размера выборки.
