# Проведите тест гипотезы. Продавец утверждает, 
# что средний вес пачки печенья составляет 200 г. 
# Из партии извлечена выборка из 10 пачек. 
# Вес каждой пачки составляет: 202, 203, 199, 197, 195, 201, 200, 204, 194, 190. 
# Известно, что их веса распределены нормально. Верно ли утверждение продавца, 
# если учитывать, что доверительная вероятность равна 99%? (Провести двусторонний тест.)

from scipy.stats import t

# заданные значения
alpha = 0.05
mu = 200
sample = [202, 203, 199, 197, 195, 201, 200, 204, 194, 190]

# вычисляем выборочное среднее (x_bar) и стандартное отклонение (s)
n = len(sample)
x_bar = sum(sample) / n
s = (sum([(x - x_bar) ** 2 for x in sample]) / (n - 1)) ** 0.5

# вычисляем t-статистику
t_stat = (x_bar - mu) / (s / n ** 0.5)

# вычисляем число степеней свободы
df = n - 1

# вычисляем критическое значение t
t_crit = abs(t.ppf(alpha / 2, df))

# вычисляем p-значение
p_value = 2 * (1 - t.cdf(abs(t_stat), df))

# сравниваем с уровнем значимости
if abs(t_stat) > t_crit:
    print('Отвергаем H0 в пользу H1')
else:
    print('Не удалось отвергнуть H0')

print('t-статистика:', t_stat)
print('Критическое значение t:', t_crit)
print('p-значение:', p_value)


# Результаты теста гипотезы:
# Отвергаем H0 в пользу H1
# t-статистика: -1.0651074037450896
# Критическое значение t: 2.262157162740992
# p-значение: 0.31456660352472854


# Таким образом, мы отвергаем нулевую гипотезу и можем сделать вывод, 
# что утверждение продавца о среднем весе пачки печенья 200 г 
# неверно на уровне значимости 0.01. Средний вес пачки печенья 
# значимо отличается от 200 г.