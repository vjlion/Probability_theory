# Устройство состоит из трех деталей. Для первой детали вероятность 
# выйти из строя в первый месяц равна 0.1, для второй - 0.2, 
# для третьей - 0.25. Какова вероятность того, что в первый месяц 
# выйдут из строя: а). все детали б). только две детали 
# в). хотя бы одна деталь г). от одной до двух деталей?

# Для решения задачи можно использовать биномиальное распределение - 
# если обозначить вероятность выхода из строя детали как p, то число 
# вышедший из строя деталей может быть определено как случайная величина, 
# имеющая биномиальное распределение с параметрами (n, p).

# a) Вероятность того, что все детали выйдут из строя:

# P = 0.1 * 0.2 * 0.25 = 0.005

# б) Вероятность того, что только две детали выйдут из строя:
    
# P = 0.1 * 0.2 * 0.75 + 0.1 * 0.8 * 0.25 + 0.9 * 0.2 * 0.25 = 0.08

# в) Вероятность того, что хотя бы одна деталь выйдет из строя:

# P = 1 - (0.9 * 0.8 * 0.75) = 0.46

# г) Вероятность того, что выйдет из строя от одной до двух деталей:

# P = (0.1 * 0.8 * 0.75 + 0.9 * 0.2 * 0.75 + 0.9 * 0.8 * 0.25) + (0.1 * 0.2 * 0.25) = 0.345



from scipy.special import comb

# Вероятность выхода из строя каждой детали
p1 = 0.1
p2 = 0.2
p3 = 0.25

# Общее число деталей
n = 3

# Вероятность того, что все детали выйдут из строя
P_a = p1 * p2 * p3

# Вероятность того, что только две детали выйдут из строя
P_b = p1 * p2 * (1 - p3) + p1 * (1 - p2) * p3 + (1 - p1) * p2 * p3

# Вероятность того, что хотя бы одна деталь выйдет из строя
P_c = 1 - (1 - p1) * (1 - p2) * (1 - p3)

# Вероятность того, что выйдет из строя от одной до двух деталей
P_d = (p1 * (1 - p2) * (1 - p3) + (1 - p1) * p2 * (1 - p3) + (1 - p1) * (1 - p2) * p3) + (p1 * p2 * p3)

# Округление до трех знаков после запятой
P_a = round(P_a, 3)
P_b = round(P_b, 3)
P_c = round(P_c, 3)
P_d = round(P_d, 3)

print("a) Вероятность того, что все детали выйдут из строя:", P_a)
print("б) Вероятность того, что только две детали выйдут из строя:", P_b)
print("в) Вероятность того, что хотя бы одна деталь выйдет из строя:", P_c)
print("г) Вероятность того, что выйдет из строя от одной до двух деталей:", P_d)
 

